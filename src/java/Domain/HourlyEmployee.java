package Domain;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.validation.constraints.NotNull;

/**
 *
 * @author Armando.Perea
 */
@Entity
public class HourlyEmployee extends Employee implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    
    @NotNull
    private Float hourlyRate;

    /**
     * Get the value of hourlyRate
     *
     * @return the value of hourlyRate
     */
    public Float getHourlyRate() {
        return hourlyRate;
    }

    /**
     * Set the value of hourlyRate
     *
     * @param hourlyRate new value of hourlyRate
     */
    public void setHourlyRate(Float hourlyRate) {
        this.hourlyRate = hourlyRate;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof HourlyEmployee)) {
            return false;
        }
        HourlyEmployee other = (HourlyEmployee) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Domain.HourlyEmployee[ id=" + id + " ]";
    }

}
